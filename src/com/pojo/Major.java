package com.pojo;

public class Major {
	private int id;//专业id
	private String mname;//主页名字
	private int stunum;//人数
	private int money;//学费
	private int classtime;//课时
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public int getStunum() {
		return stunum;
	}
	public void setStunum(int stunum) {
		this.stunum = stunum;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getClasstime() {
		return classtime;
	}
	public void setClasstime(int classtime) {
		this.classtime = classtime;
	}
	
	
}
